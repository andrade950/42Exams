#include <unistd.h>

int	main(int ac, char **av)
{
	int i = 0;
	int end = 0;

	if (ac == 2)
	{
		while (av[1][i])
			i++;
		while (av[1][i] == '\0' || av[1][i] == ' ' || av[1][i] == '\t')
			i--;
		end = i + 1;
		i = 0;
		while (av[1][i])
		{
			while (av[1][i] && (av[1][i] == ' ' || av[1][i] == '\t'))
				i++;
			while (av[1][i] && av[1][i] != ' ' && av[1][i] != '\t')
			{
				write(1, &av[1][i], 1);
				i++;
				if ((av[1][i] == ' ' || av[1][i] == '\t') && end != i)
					write(1, "   ", 3);
			}
		}
	}
	write(1, "\n", 1);
}
